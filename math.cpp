#include <iostream>

int main()
{
int a = 10;
int b = 7;
std::cout<<"The sum of 10 and 7 is "<<(a+b)<<std::endl;
std::cout<<"The difference of 10 and 7 is "<<(a-b)<<std::endl;
std::cout<<"The product of 10 and 7 is "<<(a*b)<<std::endl;
std::cout<<"The division of 10 and 7 is "<<(double)a/(double)b<<std::endl;
std::cout<<"The modulus of 10 and 7 is "<<(a%b)<<std::endl;
return 0;
}